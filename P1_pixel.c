#include <graphics.h>
#include <conio.h>

int main() {
    int gd = DETECT, gm;
    initgraph(&gd, &gm, "");  // Initialize graphics mode

    putpixel(100, 100, WHITE);  // Draw a white pixel at (100, 100)

    getch();  // Wait for a key press
    closegraph();  // Close graphics mode
    return 0;
}
